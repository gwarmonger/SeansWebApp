<h3>Welcome to Sean's demo app.</h3>
<h6>*This is a beta release.</h6>
<p>The app consists of a login system. Once registered and logged in, other options become available in the navigation</p>
<p>I have used Codeigniter PHP framework, Bootstrap CSS framework, and Jquery/Jquery-UI</p>
<p>My goals in creating this app were to hone my PHP skills and also to learn how to use MVC pattern for programming. All of these goals have been met.</p>
<p>The app consists of three main sections:	</p>
<ul class="introul">
	<li>1. The Memo section allows the user to add a memo. The memo enters the database and then are accessible by the user
		by either clicking the calendar or the date buttons. The memos can be dragged, and once the mouse button is let go, 
		the position is saved to the database and will remain in the same position upon page reload</li>
	</br>
	<li>2. The Songshare section allows the user to upload an Mp3 or Wav file. The songs are then added to a table in the view. 
	I've also made a custom html5 audio player that includes play, pause and progress functions. 
	</li>
</br>
	<li>3. The Movie section allows you to search for movies. I used Rotten Tomatoes movie website's API to pull in various feeds.
		I have also used NPR and NY Times APIs to pull in movie articles. The user can also create a category and then add movies to their categories.
	</li>
	</br>
	<li>The site is responsive (though I have not tested extensively yet across devices).</li>
	</br>
	<li>My code is available for viewing at <a href="https://github.com/gwarmonger/SeansWebApp">https://github.com/gwarmonger/SeansWebApp</a></li>
</ul> 
